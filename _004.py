myName = "David"

myName.upper() # prints "DAVID"

myName.center(10) #   David   ' -> returns a string centered in a field of size 10

myName.find('v') # prints 2

myName.split('v') # prints ['Da', 'id'] -> Splits a string into substrings at 'v'


